    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String FName = jTextField2.getText();    
String Eml = jTextField3.getText();        
char[] password = jPasswordField3.getPassword(); // Use getPassword() instead of getText()

// Validate the input fields
if (FName.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Please enter your full name.");
    return;
}
if (Eml.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Please enter your email.");
    return;
}
if (password.length == 0) {
    JOptionPane.showMessageDialog(null, "Please enter a password.");
    return;
}

try (FileWriter writer = new FileWriter("Registration.txt", true)) {
    writer.write(FName + " " + Eml + " " + new String(password));
    writer.write(System.lineSeparator()); // Use System.lineSeparator() correctly

    // Notify the user of a successful registration
    JOptionPane.showMessageDialog(null, "Registration Successful!");

    // Clear the input fields for a fresh start
    jTextField2.setText("");
    jTextField3.setText("");
    jPasswordField3.setText("");
} catch (IOException e) {
    // Provide a more specific error message
    JOptionPane.showMessageDialog(null, "Error while saving registration: " + e.getMessage());
}


    }//GEN-LAST:event_jButton1ActionPerformed

